package lesson4.while_do;

public class DoWhile {
    public static void main(String[] args) {
        int i =0;
        //do while выполняет программу хотя бы один раз
        do{
            System.out.println("message");
        }while (i<0);

        while (i<0){
            System.out.println("message1");
        }
    }

}
